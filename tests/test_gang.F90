SUBROUTINE CHECK_STATUS

INTEGER (KIND=JPIM) :: JFLD

DO JFLD = 1, NDIM
  IF (YLF4%ISTATUS /= YLF3L(JFLD)%PTR%ISTATUS) THEN
    PRINT *, JFLD
    WRITE (*, '("YLF4 = ",Z16.16)') YLF4%ISTATUS
    WRITE (*, '("YLF3L (",I0,")=",Z16.16)') JFLD, YLF3L(JFLD)%PTR%ISTATUS
    CALL ABOR1 ('STATUS MISMATCH')
  ENDIF
ENDDO

