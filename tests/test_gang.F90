PROGRAM TEST_GANG

USE FIELD_MODULE
USE FIELD_FACTORY_MODULE
USE FIELD_ACCESS_MODULE
USE PARKIND1
USE OMP_LIB, ONLY: OMP_GET_MAX_THREADS
IMPLICIT NONE

CLASS(FIELD_4RD), POINTER :: YLF4
TYPE(FIELD_3RD_PTR), ALLOCATABLE :: YLF3L (:)

INTEGER (KIND=JPIM), PARAMETER :: NPROMA = 32, NFLEVG = 15, NDIM = 3, NGPBLKS = 10
LOGICAL, PARAMETER :: LLPERSISTENT = .TRUE.

INTEGER (KIND=JPIM), PARAMETER :: ILB (4) = [1,0,1,1], IUB (4) = [NPROMA, NFLEVG, NDIM, NGPBLKS]

#include "abor1.intfb.h"

REAL (KIND=JPRD) :: ZCOEF (NDIM)

REAL (KIND=JPRD) :: FUNC
REAL (KIND=JPRD) :: PCOEF_
INTEGER (KIND=JPIM) :: JLON_, JLEV_, JFLD_, JBLK_
FUNC (JLON_, JLEV_, JFLD_, JBLK_) = REAL (JLON_, JPRD) + 100._JPRD * (REAL (JLEV_, JPRD) + 100._JPRD * &
                                 & (REAL (JFLD_, JPRD) + 100._JPRD * (REAL (JBLK_, JPRD))))



CALL NEW_YLF4

CALL SET_HOST_YLF4

CALL SET_DEVICE_YLF4

CALL SET_DEVICE_YLF3 ([1, 3])

CALL GET_HOST_YLF4 

CALL GET_DEVICE_YLF4

CALL GET_DEVICE_YLF3 ([2])

CALL DEL_YLF4

CONTAINS

SUBROUTINE NEW_YLF4

CALL FIELD_NEW (YLF4, CHILDREN=YLF3L, UBOUNDS=IUB, LBOUNDS=ILB, PERSISTENT=LLPERSISTENT)

CALL CHECK_STATUS

END SUBROUTINE

SUBROUTINE SET_VIEW_YLF4

INTEGER (KIND=JPIM) :: JLON, JLEV, JFLD, JBLK
REAL (KIND=JPRD), POINTER :: ZPTR3 (:,:,:)

CALL YLF4%SYNC_HOST_RDWR ()

!$OMP PARALLEL DO PRIVATE (ZPTR3, JBLK)
DO JBLK = 1, NGPBLKS
  ZPTR3 => YLF4%GET_VIEW (BLOCK_INDEX=JBLK)
  DO JFLD = 1, NDIM
   DO JLEV = 1, NFLEVG
     DO JLON = 1, NPROMA
       ZPTR3 (JLON, JLEV, JFLD) = FUNC (JLON, JLEV, JFLD, JBLK)
     ENDDO
   ENDDO
  ENDDO
ENDDO
!$OMP END PARALLEL DO

END SUBROUTINE

SUBROUTINE GET_VIEW_YLF4

INTEGER (KIND=JPIM) :: JLON, JLEV, JFLD, JBLK
REAL (KIND=JPRD), POINTER :: ZPTR3 (:,:,:)

CALL YLF4%SYNC_HOST_RDWR ()

!$OMP PARALLEL DO PRIVATE (ZPTR3, JBLK)
DO JBLK = 1, NGPBLKS
  ZPTR3 => YLF4%GET_VIEW (BLOCK_INDEX=JBLK)
  DO JFLD = 1, NDIM
   DO JLEV = 1, NFLEVG
     DO JLON = 1, NPROMA
       IF (ZPTR3 (JLON, JLEV, JFLD) /= FUNC (JLON, JLEV, JFLD, JBLK)) THEN
         PRINT *, ZPTR3 (JLON, JLEV, JFLD)
         PRINT *, FUNC (JLON, JLEV, JFLD, JBLK)
         CALL ABOR1 ('GET_VIEW_YLF4: VALUE MISMATCH')
       ENDIF
     ENDDO
   ENDDO
  ENDDO
ENDDO
!$OMP END PARALLEL DO

END SUBROUTINE

